package ex4_list;

public class Person {
	private String name, te1;
	private int age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTe1() {
		return te1;
	}
	public void setTe1(String te1) {
		this.te1 = te1;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
